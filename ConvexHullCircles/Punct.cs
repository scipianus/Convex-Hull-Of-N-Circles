using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ConvexHullCircles
{
    class Punct
    {
        public float x, y;

        public Punct()
        {
            this.x = this.y = 0;
        }

        public Punct(float x, float y)
        {
            this.x = x;
            this.y = y;
        }
        
    }
}
